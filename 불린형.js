/* 프로그래밍 핵심 개념 in JavaScript */
// [자료형] 08. 불린형

// 불린 (Boolean)
console.log(2 > 1);
console.log(2 < 1);
console.log(3 >= 2);
console.log(3 <= 3);
console.log(3 === 3);
console.log(3 !== 3);

console.log('Codeit' === 'Codeit');
console.log('Codeit' !== 'Codeeat');

// && : AND 연산자
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

// || : OR 연산자
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

// ! : NOT 연산자
console.log(!true);
console.log(!false);

console.log(!!true);
console.log(!!false);

