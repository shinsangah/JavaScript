// 04. 문자열 활용

// 문자열
console.log('코드잇');
console.log("I'm Iron man");

console.log("He said \"I'm Iron man\"");
console.log('He said "I\'m Iron man"');
// 양 끝에 감싸는 따옴표와 중복된 따옴표 앞에
// 역 슬래쉬 \ 를 써주면 문자 안에 기호라고 생각한다.

console.log("He said \"I\'m Iron man\"");
console.log('He said \"I\'m Iron man\"');
// 헷갈리면 내부에 있는 따옴표들 앞에 다 역슬래쉬 써주면 된다.

console.log(`He said "I'm Iron man"`);
// ` 백틱을 사용하면 된다.

